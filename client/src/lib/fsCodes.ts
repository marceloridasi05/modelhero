function hexToRgb(hex: string): { r: number; g: number; b: number } | null {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  return result
    ? {
        r: parseInt(result[1], 16),
        g: parseInt(result[2], 16),
        b: parseInt(result[3], 16),
      }
    : null;
}

function colorDistance(hex1: string, hex2: string): number {
  const rgb1 = hexToRgb(hex1);
  const rgb2 = hexToRgb(hex2);
  if (!rgb1 || !rgb2) return Infinity;
  
  const rDiff = rgb1.r - rgb2.r;
  const gDiff = rgb1.g - rgb2.g;
  const bDiff = rgb1.b - rgb2.b;
  
  return Math.sqrt(rDiff * rDiff + gDiff * gDiff + bDiff * bDiff);
}

export function findMatchingFSCode(hexColor: string, threshold: number = 30): FSCode | null {
  if (!hexColor) return null;
  
  let bestMatch: FSCode | null = null;
  let bestDistance = Infinity;
  
  for (const fsCode of FS_CODES) {
    const distance = colorDistance(hexColor, fsCode.color);
    if (distance < bestDistance && distance <= threshold) {
      bestDistance = distance;
      bestMatch = fsCode;
    }
  }
  
  return bestMatch;
}

export function findAllMatchingFSCodes(hexColor: string, threshold: number = 50): FSCode[] {
  if (!hexColor) return [];
  
  return FS_CODES
    .map(fsCode => ({
      fsCode,
      distance: colorDistance(hexColor, fsCode.color)
    }))
    .filter(item => item.distance <= threshold)
    .sort((a, b) => a.distance - b.distance)
    .map(item => item.fsCode);
}
